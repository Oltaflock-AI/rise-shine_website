/**
 * Build src/data/hotel-cities.json from TBO's static CityList API.
 *
 * Fetches the city list for every country in COUNTRIES (popular destinations
 * for the agency — domestic + Indian-outbound) and writes one compact dataset
 * the hotel search autocomplete reads. Re-run whenever TBO adds cities or you
 * want more countries:
 *
 *   node scripts/fetch-hotel-cities.mjs
 *
 * Reads TBO_HOTEL_STATIC_* from .env.local (never commits creds).
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const env = {};
for (const line of readFileSync(resolve(root, ".env.local"), "utf8").split("\n")) {
  const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
  if (m) env[m[1]] = m[2].trim();
}
const BASE = (env.TBO_HOTEL_STATIC_URL || "http://api.tbotechnology.in/TBOHolidays_HotelAPI").replace(/\/+$/, "");
const AUTH = "Basic " + Buffer.from(`${env.TBO_HOTEL_STATIC_USERNAME}:${env.TBO_HOTEL_STATIC_PASSWORD}`).toString("base64");
if (!env.TBO_HOTEL_STATIC_USERNAME) {
  console.error("Missing TBO_HOTEL_STATIC_USERNAME/PASSWORD in .env.local");
  process.exit(1);
}

/** Country coverage: India + the agency's outbound market. Extend freely. */
const COUNTRIES = [
  "IN", "AE", "TH", "SG", "MY", "ID", "VN", "KH", "LK", "NP", "BT", "MV",
  "HK", "MO", "CN", "JP", "KR", "PH", "QA", "OM", "SA", "BH", "KW", "JO",
  "IL", "TR", "EG", "KE", "TZ", "ZA", "MU", "SC", "AU", "NZ", "US", "CA",
  "GB", "FR", "IT", "CH", "ES", "DE", "NL", "AT", "BE", "CZ", "HU", "GR",
  "PT", "AZ", "GE", "KZ", "UZ", "RU",
];

async function cityList(cc) {
  const r = await fetch(`${BASE}/CityList`, {
    method: "POST",
    headers: { Authorization: AUTH, "Content-Type": "application/json" },
    body: JSON.stringify({ CountryCode: cc }),
  });
  const j = await r.json();
  if (j?.Status?.Code !== 200) throw new Error(`${cc}: Status ${j?.Status?.Code} ${j?.Status?.Description ?? ""}`);
  return j.CityList ?? [];
}

const out = [];
let done = 0;
const queue = [...COUNTRIES];
async function worker() {
  while (queue.length) {
    const cc = queue.shift();
    try {
      const cities = await cityList(cc);
      for (const c of cities) {
        const name = String(c.Name ?? "").replace(/\s+/g, " ").replace(/\s*,\s*/g, ", ").trim();
        if (!c.Code || !name) continue;
        out.push({ c: String(c.Code), n: name, cc });
      }
      console.log(`${cc}: ${cities.length} cities  (${++done}/${COUNTRIES.length})`);
    } catch (e) {
      console.error(`${cc}: FAILED — ${e.message}  (${++done}/${COUNTRIES.length})`);
    }
  }
}
await Promise.all(Array.from({ length: 5 }, worker));

out.sort((a, b) => a.n.localeCompare(b.n));
const json = JSON.stringify(out);
// Emit a .ts wrapping JSON.parse of ONE string literal — typechecking 43k object
// literals is what makes tsc crawl; a string literal keeps it fast.
const ts = `/* AUTO-GENERATED by scripts/fetch-hotel-cities.mjs — do not edit by hand.
 * TBO hotel cities (${out.length} rows). JSON.parse keeps the
 * TypeScript checker fast: one string literal instead of 43k object literals. */
export type HotelCityRow = { c: string; n: string; cc: string };
export const HOTEL_CITY_DATA: HotelCityRow[] = JSON.parse(${JSON.stringify(json)});
`;
const file = resolve(root, "src/data/hotel-cities.generated.ts");
writeFileSync(file, ts);
console.log(`\nWrote ${out.length} cities → ${file} (${Math.round(ts.length / 1024)} KB)`);
